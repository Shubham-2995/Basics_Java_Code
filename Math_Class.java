package Basictesting;

public class Math_Class 
{

	public static void main(String[] args)
	{

		System.out.println( Math.addExact(45, 85));
		System.out.println( Math.addExact(4555555555l, 78));
		System.out.println( Math.subtractExact(100, 92));
		System.out.println( Math.multiplyExact(12, 13));
		System.out.println( Math.divideExact(120, 5));
		System.out.println( Math.max(4, 12));
		System.out.println( Math.min(-1, 2));
		System.out.println( Math.sqrt(81));
		System.out.println( Math.random());     // random gives you unique value each time you run it between 0 and 1 and it's answer will be double
		System.out.println( Math.abs(-120));
		System.out.println( Math.PI);
		
		
	}

}
